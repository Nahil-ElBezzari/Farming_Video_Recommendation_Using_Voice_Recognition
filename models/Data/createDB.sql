
CREATE TABLE `o_tag` (
  `ID_O_TAG` int NOT NULL AUTO_INCREMENT,
  `TAG` varchar(45) NOT NULL,
  PRIMARY KEY (`ID_O_TAG`),
  UNIQUE KEY `ID_O_TAG_UNIQUE` (`ID_O_TAG`)
) ENGINE=InnoDB AUTO_INCREMENT=4 DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

CREATE TABLE `o_video` (
  `id` int NOT NULL AUTO_INCREMENT,
  `video_title` varchar(255) DEFAULT NULL,
  `url` varchar(255) DEFAULT NULL,
  PRIMARY KEY (`id`)
) ENGINE=InnoDB AUTO_INCREMENT=78 DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

CREATE TABLE T_VIDEO_TAG (
    ID_T_VIDEO_TAG INT PRIMARY KEY AUTO_INCREMENT,
    FK_O_TAG INT,
    FK_O_VIDEO INT,
    FOREIGN KEY (FK_O_TAG) REFERENCES o_tag(id_o_tag),
    FOREIGN KEY (FK_O_VIDEO) REFERENCES o_video(id)
);